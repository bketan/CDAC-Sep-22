class Q6
{
	public static void main(String args[])
	{
		for(int i =20;i>0;i--)   //using for loop
		{
			System.out.print(i+" ");
		}
		System.out.println();
    
		int j=20;               //using while loop
		while(j>0)
		{
			System.out.print(j+" ");
			j--;
		}
		System.out.println();
   
		int k =20;              //using do while loop
		do
		{
			System.out.print(k+" ");
			k--;
		}while(k>0);
	}
}